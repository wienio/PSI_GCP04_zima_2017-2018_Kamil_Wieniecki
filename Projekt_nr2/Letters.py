def getLetter(size, letter):
    "Zwraca wektor dla danej litery (podzial na 6 obszarow), przy sektorach i - kolumny, j - wiersze"
    result = []
    for i in range (0,7):
        result.append(0)
    result[0] = 1

    #obszar nr1 - lewy dolny rog
    for i in range (3, 7):
        for j in range(0, 2):
            if letters[size][letter][i][j] == 1:
                result[1] = 1

    #obszar nr2 - srodek na dole
    for i in range (3, 7):
        for j in range(2, 3):
            if letters[size][letter][i][j] == 1:
                result[2] = 1
    
    #obszar nr3 - prawy dolny rog
    for i in range (3, 7):
        for j in range(3, 5):
            if letters[size][letter][i][j] == 1:
                result[3] = 1

    #obszar nr4 - lewy gorny rog
    for i in range (0, 3):
        for j in range(0, 2):
            if letters[size][letter][i][j] == 1:
                result[4] = 1

    #obszar nr5 - srodek na gorze
    for i in range (0, 3):
        for j in range(2, 3):
            if letters[size][letter][i][j] == 1:
                result[5] = 1
    
    #obszar nr6 - prawy gorny rog
    for i in range (0, 3):
        for j in range(3, 5):
            if letters[size][letter][i][j] == 1:
                result[6] = 1

    return result
